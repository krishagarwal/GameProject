	public void drawBoard(Graphics g, int rowStart, int rowEnd, int colStart, int colEnd, int refX, int refY)
	{
		for (int i = 0; i < rowEnd - rowStart; i++)
		{
			for (int j = 0; j < colEnd - colStart; j++)
			{
				g.setColor(new Color(40, 130, 75));
				if (i + rowStart < 0 || j + colStart < 0 || i + rowStart > 24 || j + colStart > 24)
					g.fillRect(i * 40 + refX, j * 40 + refY, 40, 40);
				else
				{
					g.setColor(new Color(45, 235, 115));
					g.fillRect(i * 40 + refX, j * 40 + refY, 40, 40);
					g.setColor(Color.BLACK);
					char c = total[i + rowStart][j + colStart];
					if (c == 'w' || c == '1' || c == '2')
						g.fillRect(i * 40 + refX, j * 40 + refY, 2, 40);
					if (c == 'd' || c == '2' || c == '4')
						g.fillRect(i * 40 + refX, j * 40 + 38 + refY, 40, 2);
					if (c == 's' || c == '3' || c == '4')
						g.fillRect(i * 40 + 38 + refX, j * 40 + refY, 2, 40);	
					if (c == 'a' || c == '1' || c == '3')
						g.fillRect(i * 40 + refX, j * 40 + refY, 40, 2);
				}
			}
		}
	}
