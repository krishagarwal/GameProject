	public void drawBoard(Graphics g, int rowStart, int rowEnd, int colStart, int colEnd)
	{
		for (int i = 0; i <= rowEnd - rowStart; i++)
		{
			for (int j = 0; j < colEnd - colStart; j++)
			{
				g.setColor(Color.BLACK);
				if (i + rowStart <= 0 || i + rowStart <= 0)
					g.drawRect(i * 40, j * 40, 40, 40);
				else
				{
					char c = total[i + rowStart][j + colStart];
					if (c == 's')
						g.drawRect((i + 1) * 40, j * 40, 2, 40);
					else if (c == 't')
						g.drawRect(i * 40, j * 40, 40, 2);
				}
			}
		}
	}
